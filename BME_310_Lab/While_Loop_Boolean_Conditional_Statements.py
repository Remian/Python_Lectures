# iteration - 20 --> run till 20
#boolean variable

x = False
y = 0

while(x==False):

    y = y+1

    if(y==20):
        x = True
        print("\nReached 20 Iterations. Loop Ended!")

    elif(y==10):
        print("\nHalf Way Through!\n")

    else:
        print("Iteration: " + str(y))

